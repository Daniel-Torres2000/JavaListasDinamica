public void Imprimir_TableGeneral(){
    Nodo iterador = playlist.primero; //declaramos el iterador 
    //limpiamos la tabla general
    DefaultTableModel modelGeneral = (DefaultTableModel) jtGeneral.getModel();
    modelGeneral.addRow(new Object[]{" "});
    for (int i = 0; i < jtGeneral.getRowCount(); i++) 
    {
        modelGeneral.removeRow(i);
        i-=1;
    }
    //limpiamos la tabla de salsa
    DefaultTableModel modelSalsa = (DefaultTableModel) jTsalsa.getModel();
    modelSalsa.addRow(new Object[]{" "});
    for (int i = 0; i < jTsalsa.getRowCount(); i++) 
    {
        modelSalsa.removeRow(i);
        i-=1;
    }
    //limpiamos la tabla de Rock
    DefaultTableModel modelRock = (DefaultTableModel) jTrock.getModel();
    modelRock.addRow(new Object[]{" "});
    for (int i = 0; i < jTrock.getRowCount(); i++) 
    {
        modelRock.removeRow(i);
        i-=1;
    }
    while(iterador.getCancion()!=null)
    {
        modelGeneral.addRow(new Object[]{iterador.getCancion()});
        if(iterador.getGenero()==1)
        {
            modelSalsa.addRow(new Object[]{iterador.getCancion()});
        }else{
            modelRock.addRow(new Object[]{iterador.getCancion()});
        }
        iterador = iterador.siguiente;
    }
}
